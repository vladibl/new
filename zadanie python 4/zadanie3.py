#Вычислить число c заданной точностью *d*


pi = 3.141592653589793238462643383279502884197169399375105820974944592307816406286208998

d = int(input('\nЗадайте точность вычисления числа Пи  d = '))

S1 = 3.0
z = 1 
i = 2

while abs(pi - S1) > 0.1**d:
    S1 =S1+4*z/(i*(i+1)*(i+2))
    i = i+2
    z = -z
print(round(S1, d))